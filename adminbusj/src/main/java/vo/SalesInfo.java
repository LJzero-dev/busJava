package vo;

public class SalesInfo {
	private String bl_type, start_spot, end_spot;
	private int cardP, bankP, easyP, count_schedule;
	public String getBl_type() {
		return bl_type;
	}
	public void setBl_type(String bl_type) {
		this.bl_type = bl_type;
	}
	public String getStart_spot() {
		return start_spot;
	}
	public void setStart_spot(String start_spot) {
		this.start_spot = start_spot;
	}
	public String getEnd_spot() {
		return end_spot;
	}
	public void setEnd_spot(String end_spot) {
		this.end_spot = end_spot;
	}
	public int getCardP() {
		return cardP;
	}
	public void setCardP(int cardP) {
		this.cardP = cardP;
	}
	public int getBankP() {
		return bankP;
	}
	public void setBankP(int bankP) {
		this.bankP = bankP;
	}
	public int getEasyP() {
		return easyP;
	}
	public void setEasyP(int easyP) {
		this.easyP = easyP;
	}
	public int getCount_schedule() {
		return count_schedule;
	}
	public void setCount_schedule(int count_schedule) {
		this.count_schedule = count_schedule;
	}
	
	
}
