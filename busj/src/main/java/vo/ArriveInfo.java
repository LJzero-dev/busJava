package vo;

public class ArriveInfo {
	private String bs_stime, bs_etime, leftTime, status, bc_company, bi_level, bi_num;

	public String getBs_stime() {
		return bs_stime;
	}

	public void setBs_stime(String bs_stime) {
		this.bs_stime = bs_stime;
	}

	public String getBs_etime() {
		return bs_etime;
	}

	public void setBs_etime(String bs_etime) {
		this.bs_etime = bs_etime;
	}

	public String getLeftTime() {
		return leftTime;
	}

	public void setLeftTime(String leftTime) {
		this.leftTime = leftTime;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getBc_company() {
		return bc_company;
	}

	public void setBc_company(String bc_company) {
		this.bc_company = bc_company;
	}

	public String getBi_level() {
		return bi_level;
	}

	public void setBi_level(String bi_level) {
		this.bi_level = bi_level;
	}

	public String getBi_num() {
		return bi_num;
	}

	public void setBi_num(String bi_num) {
		this.bi_num = bi_num;
	}
	
}
