package vo;

public class SeatInfo {
	private int si_idx, bi_idx;
	private String si_seat, si_isuse;
	
	public int getSi_idx() {
		return si_idx;
	}
	public void setSi_idx(int si_idx) {
		this.si_idx = si_idx;
	}
	public int getBi_idx() {
		return bi_idx;
	}
	public void setBi_idx(int bi_idx) {
		this.bi_idx = bi_idx;
	}
	public String getSi_seat() {
		return si_seat;
	}
	public void setSi_seat(String si_seat) {
		this.si_seat = si_seat;
	}
	public String getSi_isuse() {
		return si_isuse;
	}
	public void setSi_isuse(String si_isuse) {
		this.si_isuse = si_isuse;
	}
	
}
